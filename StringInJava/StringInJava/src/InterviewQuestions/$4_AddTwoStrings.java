package InterviewQuestions;

public class $4_AddTwoStrings {
    /*
    Question:- How to add two string ?
    Answer:- Using Integer.parseInt() method.
     */
    public static void main(String[] args) {

        String s1 = "10";
        String s2 ="71";
        int i = Integer.parseInt(s1) + Integer.parseInt(s2);
        System.out.println("The sum is  "+ i);
    }
}
